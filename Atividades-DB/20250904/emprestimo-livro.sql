    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    CONSTRAINT PK_EMPRESTIMO PRIMARY KEY(ID),
    ID_EXEMPLAR NUMBER NOT NULL REFERENCES EXEMPLARES(ID),
    DATA_RETIRADA DATE,
    DATA_PREVISTA DATE,
    DATA_DEVOLUCAO DATE
);

CREATE TABLE EXEMPLARES(
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    CONSTRAINT PK_EXEMPLARES PRIMARY KEY (ID),
    ESTADO VARCHAR,
    DISPONIBILIDADE BOOLEAN,
    ISBN VARCHAR NOT NULL REFERENCES LIVROS(ISBN)
);

CREATE TABLE LIVROS(
    ISBN NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    CONSTRAINT PK_LIVROS PRIMARY KEY (ISBN),
    ANO_PUBLICACAO INTEGER (4)
);

