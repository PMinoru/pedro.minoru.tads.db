
CREATE TABLE PROFESSORES (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    CONSTRAINT PK_PROFESSORES PRIMARY KEY (ID),
    NOME VARCHAR(100) NOT NULL,
    PROFESSOR_PRESENTE BOOLEAN,
    SALA VARCHAR NOT NULL
);

CREATE TABLE DISCIPLINAS (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    CONSTRIANT PK_DISCIPLINAS PRIMARY KEY(ID),
    NOME VARCHAR(100) NOT NULL,
    SALA VARCHAR NOT NULL
);

CREATE TABLE PROFESSOR_DISCIPLINA (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    CONSTRAINT PK_PROFESSOR_DISCIPLINA PRIMARY KEY (ID),
    PROFESSOR_ID NUMBER NOT NULL REFERENCES PROFESSORES(ID),
    DISCIPLINA_ID NUMBER NOT NULL REFERENCES DISCIPLINAS(ID),
    CONSTRAINT FK_PROFESSOR_DISCIPLINA UNIQUE (PROFESSOR_ID, DISCIPLINA_ID)
);