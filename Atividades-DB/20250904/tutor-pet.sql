CREATE TABLE TUTOR(
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    CONSTRAINT PK_TUTOR PRIMARY KEY (ID),
    NOME VARCHAR(100) NOT NULL,
    IDADE NUMBER(3) CHECK (IDADE >=18 AND IDADE <=115),
    CPF NUMBER(11),
    RG NUMBER (9)
);

CREATE TABLE PET(
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    CONSTRAINT PK_PET PRIMARY KEY(ID),
    NOME VARCHAR(100) NOT NULL,
    IDADE NUMBER(2) CHECK (IDADE >=0 AND IDADE <= 40),
    TIPO_PET VARCHAR(100) NOT NULL,
    RACA_PET VARCHAR(100) NOT NULL,
    ID_TUTOR NUMBER NOT NULL,
    CONSTRAINT FK_TUTOR_DONO FOREIGN KEY(ID_TUTOR)
    REFERENCES TUTOR(ID)
);

CREATE TABLE PET_TUTOR (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    CONSTRAINT PK_PET_TUTOR PRIMARY KEY (ID),
    TUTOR_ID NUMBER NOT NULL REFERENCES TUTOR(ID),
    PET_ID NUMBER NOT NULL REFERENCES PET(ID),
    CONSTRAINT FK_PET_TUTOR UNIQUE (TUTOR_ID, TUTOR_NOME, PET_ID)
);