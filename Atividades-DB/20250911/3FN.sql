CREATE TABLE FUNCIONARIOS(
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    CONSTRAINT PK_FUNCIONARIOS PRIMARY KEY (ID),
    NOME_FUNCIONARIO VARCHAR(100) NOT NULL
);


CREATE TABLE GESTORES(
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    CONSTRAINT PK_GESTORES PRIMARY KEY (ID),
    NOME_GESTORES VARCHAR(100)
);

CREATE TABLE DEPARTAMENTO(
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    CONSTRAINT PK_DEPARTAMENTO PRIMARY KEY (ID),
    NOME_DEPARTAMENTO VARCHAR(100),
    FUNCIONARIOS_DEP NUMBER NOT NULL REFERENCES FUNCIONARIOS(ID),
    GESTORES_DEP NUMBER NOT NULL REFERENCES GESTORES(ID),
    CONSTRAINT FK_MEMBROS_DEPARTAMENTO UNIQUE(FUNCIONARIOS_DEP, GESTORES_DEP)
);