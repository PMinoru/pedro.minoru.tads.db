-- Relação de atendimentos com dados de paciente, médico e leito
SELECT 
    A.ID,
    P.NOME AS PACIENTE,
    M.ESPECIALIDADE AS MEDICO,
    L.ID,
    S.NOME_SETOR,
    A.DIAGNOSTICO
FROM ATENDIMENTO A
JOIN PACIENTE P ON A.ID_PACIENTE = P.ID
JOIN MEDICO M ON A.ID_MEDICO = M.ID
LEFT JOIN LEITO L ON A.ID_LEITO = L.ID
LEFT JOIN SETOR S ON L.ID_SETOR = S.ID;

-- Total de atendimentos por setor
SELECT 
    S.NOME_SETOR,
    COUNT(*) AS TOTAL_ATENDIMENTOS
FROM ATENDIMENTO A
JOIN LEITO L ON A.ID_LEITO = L.ID
JOIN SETOR S ON L.ID_SETOR = S.ID
GROUP BY S.NOME_SETOR
HAVING COUNT(*) > 0;

-- Pacientes com mais de um atendimento
SELECT 
    P.NOME,
    COUNT(A.ID) AS QTD_ATENDIMENTOS
FROM PACIENTE P
JOIN ATENDIMENTO A ON A.ID_PACIENTE = P.ID
GROUP BY P.NOME
HAVING COUNT(A.ID) > 1;

-- Leitos ocupados por setor
SELECT 
    L.ID,
    L.STATUS_LEITO,
    S.NOME_SETOR
FROM LEITO L
JOIN SETOR S ON L.ID_SETOR = S.ID
WHERE L.STATUS_LEITO = 'OCUPADO';

-- Total recebido por tipo de pagamento
SELECT 
    TIPO_PAGAMENTO,
    SUM(VALOR) AS TOTAL
FROM FINANCEIRO
GROUP BY TIPO_PAGAMENTO;

SELECT 
    P.ID,
    P.NOME
FROM PACIENTE P
WHERE NOT EXISTS (
    SELECT 1
    FROM ATENDIMENTO A
    WHERE A.ID_PACIENTE = P.ID
);