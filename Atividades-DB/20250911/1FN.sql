CREATE TABLE ALUNOS(
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    CONSTRAINT PK_ALUNO_ID PRIMARY KEY (ID),
    NOME VARCHAR(100),
    IDADE NUMBER (3) CHECK (IDADE >=18 AND IDADE <=115),
    CPF NUMBER(11),
    RG NUMBER (9)
);

CREATE TABLE HOBBIES(
    ID VARCHAR(100),
    ALUNO_ID NUMBER NOT NULL,
    CONSTRAINT PK_HOBBIE_ID PRIMARY KEY(ID),
);

CREATE TABLE ALUNO_HOBBIES(
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ALUNO_ID NUMBER NOT NULL REFERENCES ALUNOS(ID),
    HOBBIE_ID NUMBER NOT NULL REFERENCES HOBBIE(ID),
    CONSTRAINT FK_ALUNO_HOBBIE UNIQUE (ALUNO_ID, HOBBIE_ID) 
);